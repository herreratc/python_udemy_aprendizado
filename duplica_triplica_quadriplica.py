#DUPLICAR, TRIPLICAR , QUADRIPLICAR NUMERO


#F1 DUPLICAR

def duplica (numero):
    x_dois = numero * 2
    return x_dois

def triplica (numero):
    x_tres = numero * 3
    return x_tres

def quadriplica (numero):
    x_quatro = numero * 4
    return x_quatro

print(duplica(2))
print(triplica(4))
print(quadriplica(5))